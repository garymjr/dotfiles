-- snippets
map("i", "<Tab>", function()
  if vim.snippet.jumpable(1) then
    vim.schedule(function()
      vim.snippet.jump(1)
    end)
    return
  end
  return "<Tab>"
end, { expr = true })

map("s", "<Tab>", function()
  vim.schedule(function()
    vim.snippet.jump(1)
  end)
end)

